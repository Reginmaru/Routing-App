package com.example.RoutingApp.node;

public class node {
    private String name;
    private Double x;
    private Double y;

    public String getName() {
        return name;
    }
    public Double getX() {
        return x;
    }
    public Double getY() {
        return y;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setX(Double x) {
        this.x = x;
    }
    public void setY(Double y) {
        this.y = y;
    }
}
